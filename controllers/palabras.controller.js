exports.getPalabras = (req, res, next) => {
  var query = Palabra.find({});
  query.where({
    isOcultar: false
  });
  query.exec((err, palabras) => {
    if(err) return next(err);
    if(palabras) {
      return res.json(palabras);
      //return res.send('OK');
    }
    if(!palabras) {
      return res.send('No hay palabras registradas');
    }
  });
};
