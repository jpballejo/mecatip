/////////////////////////////////////**armarNivel/////////////////////////////////////////////////////////////////////////////////////
function armarNivel(lvl) {
  Palabra.findRandom({
    nivel: lvl
  }, {}, {
    limit: 10
  }, (err, resultado) => {
    if(err) return err + 'Nivel: ' + lvl;
    if(resultado) return resultado;
  });
};
