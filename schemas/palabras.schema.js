'use-strict'
const mongoose = require('mongoose');

const Schema = mongoose.Schema;
var palabraSchema = new Schema({
  id: Schema.Types.ObjectId,
  palabra_frase: String,
  nivel: Number,
  significado:String,
  created: {
    type: Date,
    default: Date.now
  },
  isOcultar: {
    type: Boolean,
    default: false
  }
});
