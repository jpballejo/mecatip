////////////////////////////////////////////////*****DELETE*****///////////////////////////////////////////////////////////////////

palabraSchema.statics.Delete = function(next) {
    console.log('DELETE: palabras');
  var _this = this;
  _this.isOcultar = true;
  next(_this.isOcultar);
};
